# Operadores de comparacion

# Mayor que (>)
print(7 > 3)
print(3 > 7)
print(7 > 7)

# Menor que (<)
print(5 < 6)
print(6 < 5)
print(5 < 5)

# Mayor o igual que (>=)
print(2 >= 1)
print(2 >= 3)
print(2 >= 2)

# Menor o igual que (<=)
print(1 <= 2)
print(2 <= 1)
print(2 <= 2)

# Igualacion (==)
print(6 == 6)
print(6 == 2)

# Diferente de (!=)
print(6 != 10)
print(6 != 6)

# Operadores con strings
print("Apple" == "Apple")
print("Apple" == "apple")
print("1" == 1)

# Variables
age = 18
print(age >= 18)

