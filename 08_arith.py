
# Operadores aritmeticos

# Operador para suma (+)
print(10 + 10)
# Operador para resta (-)
print(10 - 5)
# Operador para multiplicacion (*)
print(10 * 2)
# Operador para division (/)
print(10 / 2)
# Operador Modulo (%)
print(10 % 2)
print(10 / 3)
print(10 % 3)
# Operador de
print(10 // 3)
# Operador de exponenciacion
print(2**3)
# El orden se da en PEMDAS
# P = Parentesis
# E = exponentES
# M = Multiplicacion
# D = Division
# A = Adicion
# S = Sustraccion
print(2**3 + 3 - 7 / 1 // 4)
# Paso a paso de la siguiente forma
print(2**3)
print((7 / 1) // 4)
print(3 - 7)

# Operadores en cadenas de textos (strings)
print("Hola" + " Mundo")
# Operador de multiplicacion
print("Hola " * 3)

